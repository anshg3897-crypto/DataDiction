import requests
import numpy as np
import pandas as pd
from bs4 import BeautifulSoup

url="https://raw.githubusercontent.com/anshg3897-crypto/DataDiction/refs/heads/main/End_eval/dataset.html"
response=requests.get(url)

soup=BeautifulSoup(response.content,"html.parser")
soup

table = soup.find_all("table")[0]
table

table_title=table.find_all("th")
table_title

column_title=[title.text for title in table_title]
column_title

df=pd.DataFrame(columns=column_title)

df

column_data=table.find_all("tr")
column_data

for row in column_data[1:]:
  row_content = row.find_all('td')
  row_value = [data.text for data in row_content]
  length = len(df)
  df.loc[length] = row_value

df

df.dtypes

df = df.apply(pd.to_numeric, errors='coerce')
df

df.dtypes

df.to_csv("Data.csv",index=False)

